def py():
    f=open('padma.txt','a+')
    f.write('pyramid \n')
    for i in range(1,6):
        for k in range(5,i,-1):
            f.write(' ')
        for j in range(0,i):
            f.write(str(i)+' ')
        f.write('\n')
    f.close()
py()

